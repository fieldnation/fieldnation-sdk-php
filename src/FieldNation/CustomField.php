namespace FieldNation;

class CustomField implements CustomFieldInterface
{
    use IdentifiableTrait;

    private $name;

    /**
     * Set the name
     *
     * @param string $name
     * @return self
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    /**
     * Get the name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
